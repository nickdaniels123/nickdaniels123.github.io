          <p>Autumn 2015 sees the publication of the first two novels carrying my byline. In my youth, I published two non-fiction titles, long forgotten and out-of-print. In the interim, my working life has been pretty itinerant, as the economy these days seems to require. I&rsquo;ve been a banker (for a short while!), a property renovator, a tutor, an editor, a writer of commercial, professional and educational copy. I even toyed with being a food entrepreneur.</p>
          <p>At the end of the 1970s, I developed an idea for a Sherlock Holmes pastiche. At that time, the original canon was still just in copyright&mdash;in contrast to the rights, highly questionable in law, presently claimed by descendants of Sir Arthur Conan Doyle&mdash;and as, by chance, I knew the identity of the then owners of the Conan Doyle literary estate, I was able to take the project, complete with the necessary permissions, to a publisher. That approach resulted in publication of <em>Prisoner of the Devil</em>. <em>Prisoner</em> was the only new Holmes novel ever endorsed by Dame Jean Conan Doyle, Sir Arthur&rsquo;s daughter and keeper of his literary flame&mdash;this, at a time when she had no financial incentive to commend the novel.</p>
          <p>The publisher commissioned Michael Hardwick to write the book. Michael was a well-known author of period fiction and a leading light of the &lsquo;Sherlockian&rsquo; community. For almost a year, Michael and I worked closely together, I concentrating on plotting&mdash;never, he admitted, his strong suit&mdash;and, in particular, on melding fiction and the actual historical events that form the background to the story. Michael also asked me to help with revisions and to write first drafts of several scenes.</p>
          <p><em>Prisoner of the Devil</em> was originally published just under Michael Hardwick&rsquo;s byline, and it was Michael who undertook a demanding promotional campaign. <em>Prisoner</em> was a sneak bestseller in several countries. It has been out-of-print for nearly 30 years, undeservedly so in my view, and I&rsquo;m therefore delighted to see it become available again for new generations of Holmes fans&mdash;and thrilled to receive a byline credit for my own modest contribution to what I believe to have been Michael Hardwick&rsquo;s greatest literary achievement.</p>
          <p>Which brings me to the other upcoming novel. <em>City of Demons</em>&mdash;which has no more to do with Satan than <em>Prisoner of the Devil</em> does, despite their titles&mdash;has simmered on a back burner for what feels like forever, though my wife assures me it is a mere decade or two. It was begotten out of the circumstance of my dividing my time between my native London and her native New York. <em>Demons</em> is first in a hoped-for series of Shiver novels. The second, <em>Resurrection Shuffle</em>, is well underway and slated for publication in 2016, though&mdash;who knows?&mdash;it could be pushed back to 2036.</p>
